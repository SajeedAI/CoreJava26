package com.mi;

public class DatatypesValues {

	public static void main(String[] args) {
		
		// Primitive data types: byte, short, int, long, float, double, char, boolean 
		// Wrapper data types:   Byte, Short, Integer, Float, Double, Character, Boolean
		
		System.out.println(Byte.MIN_VALUE  + " " + Byte.MAX_VALUE); // -128 127
		
		System.out.println(Short.MIN_VALUE + " " + Short.MAX_VALUE); // -32768 32767
		
		System.out.println(Byte.TYPE); // byte
		
		
		
	}
}
